    private static void Debug(int debugLevel, String logLine, boolean newLine) {
        if(debugLevel > DEBUG_LEVEL) return;
        if (newLine)
            System.out.println(logLine);
        else
            System.out.print(logLine);
    }
    
    private static int DEBUG_LEVEL = 0;
    
    static 
    {
        String skippedNames = "";
        String[][] shipNames = {bigShips, ships};
        SectFile[] sectFiles = {Statics.getShipsFile(), Statics.getTechnicsFile()};
        for (int i = 0; i<2; i++) {
            for (int j=0; j<shipNames[i].length; j++) {
                String s = sectFiles[i].get(shipNames[i][j], "Mesh");
                if ((s == null) || (s.length() <= 0))
                {
                    Debug(1, shipNames[i][j] + " loading skipped, ship doesn't exist!", true);
                    skippedNames += ", " + shipNames[i][j];
                } else
                {
                    Debug(2, "Loading " + shipNames[i][j] + "...", false);
                    try {
                        Class class1 = Class.forName("com.maddox.il2.objects.ships.ShipPack$" + shipNames[i][j]);
                        if (i==0)
                            new BigshipGeneric.SPAWN(class1);
                        else
                            new ShipGeneric.SPAWN(class1);
                        Debug(2, " success!", true);
                    } catch (ClassNotFoundException e) {
                        e.printStackTrace();
                    }
                }
            }
        }
        if (skippedNames.length() != 0) System.out.println("Ships skipped from ShipPack since they don't exist in game: " + skippedNames.substring(2));
    }
